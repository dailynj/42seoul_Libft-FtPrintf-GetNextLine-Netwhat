#include "libft.h"

void	ft_lstadd_back(t_list **list, t_list *new)
{
	t_list *tmp;

	if (!*list)
		*list = new;
	else
	{
		tmp = *list;
		while (tmp->next)
			tmp = tmp->next;
		tmp->next = new;
	}
}
