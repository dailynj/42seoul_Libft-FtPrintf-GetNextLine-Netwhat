#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*result;

	if (s == 0 || f == 0)
		return (0);
	i = 0;
	while (s[i] != '\0')
		i++;
	result = malloc(sizeof(char) * (i + 1));
	if (result == 0)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
