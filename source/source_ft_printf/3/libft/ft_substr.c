#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*str;

	if (!s)
		return (0);
	if (!(str = (char *)malloc(len + 1)))
		return (0);
	if (ft_strlen(s) < start)
	{
		str[0] = 0;
		return (str);
	}
	i = 0;
	while (s[start + i] && i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = 0;
	return (str);
}
