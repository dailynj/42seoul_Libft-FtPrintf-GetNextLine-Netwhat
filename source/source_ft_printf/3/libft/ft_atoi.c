int	ft_atoi(const char *str)
{
	long	r;
	int		neg;
	int		i;

	i = 0;
	neg = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	r = 0;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		r = r * 10 + str[i++] - '0';
		if (r > 2147483647 && neg == 1)
			return (-1);
		if (r > 2147483648 && neg == -1)
			return (0);
	}
	return (r * neg);
}
